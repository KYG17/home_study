package com.gn.homework08.model.vo;

public class Person {
	
	public String name; 
	public int age; 
	
	public Person() {};
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getName() {
		return name;
	}
	
	public void setAge(int age) {
		this.age = age;
	}
	
	public int getAge() {
		return age;
	}

}
