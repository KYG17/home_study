package com.gn.study.arr;

public class Academy {
	private String name;
	private String phone;
	
	public Academy(){}
	
	public Academy(String name, String phone) {
		this.name = name;
		this.phone = phone;
	}
	
	public String getname() {
		return name;
	}
	
	public String getphone() {
		return phone;
	}
}
